    @Override
    public void EventoDeSeleccionCtaEnPUC() {
        if(bp.ObtenerCtaSeleccionada().getId().toString().length()==8){
            this.aux=bp.ObtenerCtaSeleccionada();
            this.jTextField1.setText(""+aux.getId());
            this.jTextField2.setText(aux.getDenominacion());
            this.bp.dispose();
            this.show();
        }else{
            JOptionPane.showMessageDialog(this.bp,"Debe ser un Aux contable (8 digitos) ");
        }
    }

    @Override
    public void setCta_PUC(Cta_PUC cta) {
        aux=cta;
    }
