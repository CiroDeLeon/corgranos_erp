    public void IniciarDateChooser(){
        FechaService fs=new FechaService(); 
        Calendar cal=Calendar.getInstance();
        Date d=fs.getDao().ObtenerMenorFecha();
        if(d!=null){
           cal.setTime(d);
           this.dateChooserCombo1.setSelectedDate(cal);
        }        
    }
    public void BuscarEnPuc(){
        bp=new BuscarEnPUC();
        bp.show();
        bp.setSuscripcion(this);
        this.dispose();
    } 
    public void CargarNormal(){
        m=new ResumenAuxDTOModel();
        m.setLista(service.getDao().ObtenerResumen(this.jTextField1.getText(),this.dateChooserCombo1.getSelectedDate().getTime(),this.dateChooserCombo2.getSelectedDate().getTime()));
        this.jTable1.setModel(m);      
        this.jTable1.repaint();
    }
    public void VerReporte(){
        ReporteService.VerReporte("ResumenAuxiliar.jasper",this.m.getLista(),"Resumen de Auxiliar");
    }
    void VerCtaTDetallada(){
        int fila=this.jTable1.getSelectedRow();
        VerAsientosDeAuxiliar va=new VerAsientosDeAuxiliar();
        va.setFechaInicial(this.dateChooserCombo1.getSelectedDate());
        va.setFechaFinal(this.dateChooserCombo2.getSelectedDate());
        PucService ps=new PucService();
        va.setCtaAuxiliar(ps.ObtenerCtaPuc(""+aux.getId()+""+m.getValueAt(fila,0)));
        va.Cargar();        
        va.show();
    }
