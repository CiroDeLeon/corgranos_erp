    private void ActualizarMaquila(){
        modelo=new LiquidacionDePaddyDTOModel();
        modelo.setLista(service.ObtenerLiquidaciones(maquila.getId()));
        this.jTable1.setModel(modelo);
        this.setMaquila(this.service.getMaquilaDao().ObtenerMaquila(maquila.getId()));
        this.jTable1.repaint();
    }
    private void AgregarLiquidacion(){
        if(service.AgregarLiquidacionEnMaquila(liquidacion,maquila)){
            this.setLiquidacionDePaddySinMaquila(null);
            this.jButton3.setEnabled(false);
            this.jButton4.setEnabled(false);            
        }else{
            JOptionPane.showMessageDialog(this,service.getMensaje());
        }            
    }
    private void EliminarLiquidacion(){
        if(service.EliminarLiquidacionEnMaquila(liquidacion,maquila)){
            this.setLiquidacionDePaddySinMaquila(null);
            this.jButton3.setEnabled(false);
            this.jButton4.setEnabled(false);
        }else{
            JOptionPane.showMessageDialog(this,service.getMensaje());
        }            
    }
    private void VerReporte(){
        ReporteService.VerReporte("LiquidacionDePaddyPorMaquila.jasper",this.service.ObtenerLiquidaciones(maquila.getId()),"Reporte de Maquila");
    }
