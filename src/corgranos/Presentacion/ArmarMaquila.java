    @Override
    public void setLiquidacionDePaddySinMaquila(LiquidacionDePaddyPorMaquilaDTO lp) {
        this.liquidacion=lp;
        if(lp!=null){
           Calendar cal=Calendar.getInstance();
           cal.setTime(lp.getFechacontable());
           this.dateChooserCombo1.setSelectedDate(cal);
           this.jTextField1.setText(""+lp.getNit());
           this.jTextField2.setText(""+lp.getUsuario());
           this.jFormattedTextField1.setValue(new Integer(lp.getLiquidacion()));
           this.jFormattedTextField10.setValue(new Integer(lp.getTiquete()));
           this.jFormattedTextField11.setValue(new Double(lp.getPeso14_1()));
           this.jFormattedTextField12.setValue(new Double(lp.getPesodestarado()));
           this.jFormattedTextField13.setValue(new Double(lp.getMasablanca()));
           this.jFormattedTextField14.setValue(new Double(lp.getValor()));        
        }else{
           this.jTextField1.setText("");
           this.jTextField2.setText("");
           this.jFormattedTextField1.setValue(new Integer(0));
           this.jFormattedTextField10.setValue(new Integer(0));
           this.jFormattedTextField11.setValue(new Double(0));
           this.jFormattedTextField12.setValue(new Double(0));
           this.jFormattedTextField13.setValue(new Double(0));
           this.jFormattedTextField14.setValue(new Double(0));         
        }
    }
