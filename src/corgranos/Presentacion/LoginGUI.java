    void HacerLogin(){
        String login=this.jTextField1.getText();
        String clave=this.jPasswordField1.getText();
        Actor a=service.HacerLogin(login, clave);
        if(a!=null){
            if(a.getRol().toLowerCase().equals("administrador")){
                AdministradorGUI p=new AdministradorGUI();
                p.show();
                p.setActor(a.getNombre());
                this.dispose();
            }
            if(a.getRol().toLowerCase().equals("auxiliar")){
                AuxiliarGUI p=new AuxiliarGUI();
                p.setActor(a.getNombre());
                p.show();
                this.dispose();
            }
        }else{
            JOptionPane.showMessageDialog(this,service.getMensaje());
        }
    }
