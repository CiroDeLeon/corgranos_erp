    void BuscarMaquila(){
        bm=new BuscarMaquila();
        bm.VerMaquilasCerradas=true;
        bm.Cargar();
        bm.show();
        bm.setSuscripcion(this);
        this.dispose();
    }
    void Cargar(){
        model=new ReporteMaquilaCerradaModel();
        Vector <corgranos.Dominio.Core.Maquila.ReporteMaquilaCerrada> lista=service.ObtenerReporteMaquilaCerrada(m.getId());
        if(lista.size()>0){
            this.jFormattedTextField5.setValue(new Double(lista.get(0).getTotalmasablancaproducida()));
        }else{
            this.jFormattedTextField5.setValue(new Double(0));
        }
        model.setLista(lista);
        this.jTable1.setModel(model);
        this.jTable1.repaint();
    }
