    @Override
    public void EventoDeSeleccionMaquila() {
      this.setMaquila(bm.getMaquila());        
      this.Cargar();
      this.show();
      this.requestFocus();
      bm.dispose();
    }
    @Override
    public void setMaquila(Maquila m) {
        this.m=m;
        if(m==null){
            this.jTextField1.setText("");
            this.jFormattedTextField1.setValue(new Double(0));
            this.jFormattedTextField2.setValue(new Double(0));
            this.jFormattedTextField3.setValue(new Double(0));
            this.jFormattedTextField4.setValue(new Double(0));            
        }else{
            this.jTextField1.setText(m.getId().toString());
            this.jFormattedTextField1.setValue(new Double(m.getTotalcosto()));
            this.jFormattedTextField2.setValue(new Double(m.getTotalpesodestarado()));
            this.jFormattedTextField4.setValue(new Double(m.getTotalmasablanca()));
            this.jFormattedTextField3.setValue(new Double(m.getTotalpeso14_1()));            
        }
    }
