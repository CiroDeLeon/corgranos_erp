    @Override
    public void EventoDeSeleccionArticulo() {
        this.setArticulo(ba.ObtenerArticuloSeleccionado());        
        this.ba.dispose();
        this.BuscarMaquila();
    }

    @Override
    public void setArticulo(Articulo articulo) {
        this.a=articulo;
        if(a!=null){
           this.jTextField1.setText(""+a.getId());
           this.jTextField2.setText(""+a.getDescripcion());
        }else{
           this.jTextField1.setText("");
           this.jTextField2.setText(""); 
        }
    }

    @Override
    public void EventoDeSeleccionMaquila() {
        this.setMaquila(bm.getMaquila());
        this.show();
        bm.dispose();
    }

    @Override
    public void setMaquila(Maquila m) {
        this.m=m;
        if(m!=null){
            this.jTextField3.setText(""+m.getId());
        }else{
            this.jTextField3.setText("");
        }
    }
