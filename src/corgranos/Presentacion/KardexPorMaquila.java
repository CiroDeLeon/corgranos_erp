    public void BuscarArticulo(){
        ba=new BuscarArticulo();
        ba.setSuscripcion(this);
        this.dispose();
        ba.show();
    }
    public void BuscarMaquila(){
        bm=new BuscarMaquila();
        bm.setSuscripcion(this);
        bm.VerTodaslasMaquilas=true;
        bm.Cargar();
        this.dispose();
        bm.show();
    }
    public void Cargar(){                
        model=new KardexPorMaquilaModel();
        model.setLista(is.ObtenerKardex(this.a.getId(),this.m.getId()));                       
        this.jTable1.setModel(model);
        this.jTable1.repaint();
    }
    void VerReporte(){
        ReporteService.VerReporte("KardexPorMaquila.jasper",model.getLista(),"Kardex Administrativo");
    }
