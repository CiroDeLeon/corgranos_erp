    void VerificarSiEstaCuadrado(){
        if(cs.getDao().EstaCuadradoElSistema()){             
              this.jLabel2.setIcon(new ImageIcon(getClass().getResource("ok.png")));
              this.jLabel2.setToolTipText("SISTEMA CUADRADO");
              this.jPanel3.setVisible(false);
          }else{
              this.jLabel2.setIcon(new ImageIcon(getClass().getResource("error.png")));
              this.jLabel2.setToolTipText("ERROR EN EL SISTEMA,HAY UN DESCUADRE");
              this.jPanel3.setVisible(true);
          }
    }
   
